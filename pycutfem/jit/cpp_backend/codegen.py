                        emit_line(f"    double h00 = {jloc_var}(0,0)*tmp00 + {jloc_var}(1,0)*tmp10 + d10*{hx_var}(0,0) + d01*{hy_var}(0,0);")
                        emit_line(f"    double h01 = {jloc_var}(0,1)*tmp00 + {jloc_var}(1,1)*tmp10 + d10*{hx_var}(0,1) + d01*{hy_var}(0,1);")
