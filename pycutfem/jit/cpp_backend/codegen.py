"""Generate C++ source for pybind11 kernels."""

from __future__ import annotations

import os
from pathlib import Path
from typing import Any, Iterable, Tuple
from dataclasses import replace

from .cache import CODEGEN_ABI_CPP

CODEGEN_DEBUG = bool(int(os.environ.get("PYCUTFEM_CPP_CODEGEN_DEBUG", "0")))


CPP_HEADER = """// This file is auto-generated by pycutfem C++ backend – DO NOT EDIT
#include <pybind11/pybind11.h>
#include <pybind11/numpy.h>
#include <pybind11/stl.h>
#include <Eigen/Dense>
#include <algorithm>
#include <cmath>
#include <vector>
#include <omp.h>
#include "helpers.hpp"

namespace py = pybind11;
using namespace pycutfem::cpp_backend;

"""


class CppCodeGen:
    """
    Translate the linear IR into a C++/pybind11 kernel.

    The current implementation mirrors :class:`NumbaCodeGen` to gather
    parameter ordering and active-field bookkeeping, then emits a stub
    kernel that raises a runtime error. The scaffolding keeps function
    names consistent so we can incrementally port the body to Eigen.
    """

    def __init__(
        self,
        *,
        mixed_element,
        form_rank: int = 0,
        on_facet: bool = False,
        mirror=None,
    ) -> None:
        self.mixed_element = mixed_element
        self.form_rank = form_rank
        self.on_facet = on_facet
        # Optional hook to a NumbaCodeGen instance for metadata reuse.
        self._mirror = mirror
        self.include_dirs = [Path(__file__).parent]

    # ------------------------------------------------------------------
    @staticmethod
    def kernel_export_name(default: str = "kernel") -> str:
        """Name of the callable exposed from the compiled module."""
        return default

    # ------------------------------------------------------------------
    def generate_source(
        self, ir_sequence: list, kernel_name: str, module_name: str | None = None
    ) -> Tuple[str, dict[str, Any], list[str]]:
        """
        Produce C++ source, the analytic function map, and parameter order.
        """
        if not self.on_facet:
            from pycutfem.jit.ir import LoadVariable, CheckDomain

            def _strip_side(op):
                if isinstance(op, LoadVariable):
                    if op.side or getattr(op, "field_sides", None):
                        return replace(op, side="", field_sides=None)
                    return op
                if isinstance(op, CheckDomain) and op.side:
                    return replace(op, side="")
                return op

            if any(
                (isinstance(op, LoadVariable) and (op.side or getattr(op, "field_sides", None)))
                or (isinstance(op, CheckDomain) and op.side)
                for op in ir_sequence
            ):
                ir_sequence = [_strip_side(op) for op in ir_sequence]
        analytic_map: dict[str, Any] = {}
        param_order: list[str] = []
        required_args: set[str] = set()
        ewc_rank: dict[str, int] = {}
        analytic_shapes: dict[str, tuple[int, ...]] = {}

        # IR op classes
        from pycutfem.jit.ir import (
            LoadVariable,
            LoadConstant,
            LoadConstantArray,
            LoadElementWiseConstant,
            LoadFacetNormal,
            LoadFacetNormalComponent,
            LoadAnalytic,
            CellDiameter,
            MeshSize,
            CheckDomain,
            Grad,
            PosOp,
            NegOp,
            Hessian as IRHessian,
            Laplacian as IRLaplacian,
            Div,
            HdivDiv,
            BinaryOp,
            Dot,
            Outer,
            Inner,
            Store,
            Transpose,
            Determinant,
            Inverse,
            Cofactor,
            Trace,
            PositivePartOp,
            HeavisideOp,
        )
        needs_phis = any(isinstance(op, (PosOp, NegOp)) for op in ir_sequence)
        families = getattr(self.mixed_element, "_field_families", {}) if self.mixed_element is not None else {}
        needs_any_hdiv_div = any(isinstance(op, HdivDiv) for op in ir_sequence)

        def _is_rt_field(field_name: str) -> bool:
            try:
                return isinstance(families, dict) and families.get(str(field_name)) == "RT"
            except Exception:
                return False

        def coeff_array_name(base: str, side: str) -> str:
            """Match Numba's u_<name>__pos/_neg_loc convention for sided coeffs."""
            suffix = "__pos" if side == "+" else "__neg" if side == "-" else ""
            if base.startswith("u_") and base.endswith("_loc"):
                core = base[:-4]
                return f"{core}{suffix}_loc"
            return f"u_{base}{suffix}_loc"

        if self._mirror is not None:
            try:
                _, analytic_map, param_order = self._mirror.generate_source(
                    ir_sequence, kernel_name
                )
            except Exception as exc:
                if CODEGEN_DEBUG:
                    print(
                        "[cpp_codegen] mirror codegen failed; "
                        f"falling back to IR-derived param_order: {exc}"
                    )
                # Fall back to a minimal param_order built from the IR.
                param_order = []
                needs_b: set[str] = set()
                needs_g: set[str] = set()
                needs_bvec: set[str] = set()
                needs_div: set[str] = set()
                needs_sign: set[str] = set()
                needs_det: set[str] = set()
                needs_u: set[str] = set()
                for op in ir_sequence:
                    if isinstance(op, LoadVariable):
                        for fn in op.field_names or []:
                            if _is_rt_field(str(fn)):
                                if self.on_facet and op.side in ("+", "-"):
                                    tag = "pos" if op.side == "+" else "neg"
                                    needs_bvec.add(f"bvec_{fn}_{tag}")
                                    needs_sign.add(f"sign_{fn}_{tag}")
                                    if needs_any_hdiv_div:
                                        needs_div.add(f"div_{fn}_{tag}")
                                        needs_det.add(f"detJ_{tag}")
                                else:
                                    needs_bvec.add(f"bvec_{fn}")
                                    needs_sign.add(f"sign_{fn}")
                                    if needs_any_hdiv_div:
                                        needs_div.add(f"div_{fn}")
                            else:
                                needs_g.add(f"g_{fn}")
                                if op.deriv_order == (0, 0):
                                    needs_b.add(f"b_{fn}")
                                else:
                                    needs_g.add(f"g_{fn}")
                        if op.role == "function":
                            needs_u.add(coeff_array_name(op.name, op.side))
                param_order = [
                    "gdofs_map",
                    "node_coords",
                    "qp_phys",
                    "qw",
                    "detJ",
                    "J_inv",
                    "normals",
                ]
                if needs_phis:
                    param_order.append("phis")
                param_order += [
                    *sorted(needs_b),
                    *sorted(needs_g),
                    *sorted(needs_bvec),
                    *sorted(needs_div),
                    *sorted(needs_sign),
                    *sorted(needs_det),
                    *sorted(needs_u),
                ]
        # Ensure required basis/grad tables are present even if mirror omitted them
        needs_b: set[str] = set()
        needs_g: set[str] = set()
        needs_bvec: set[str] = set()
        needs_div: set[str] = set()
        needs_sign: set[str] = set()
        needs_det: set[str] = set()
        needs_u: set[str] = set()
        needs_h: set[str] = set()
        needs_hx: bool = False
        needs_normals = False
        needs_cell_diam = False
        needs_r00: set[str] = set()
        for op in ir_sequence:
            if isinstance(op, LoadAnalytic):
                name = f"ana_{op.func_id}"
                shape = tuple(getattr(op, "tensor_shape", ()) or ())
                prev = analytic_shapes.setdefault(name, shape)
                if prev != shape:
                    raise ValueError(
                        f"Analytic shape mismatch for {name}: {prev} vs {shape}"
                    )
                if name not in param_order:
                    param_order.append(name)
            if isinstance(op, LoadConstantArray):
                # Mirror Numba's parameter ordering: Constant() objects are passed as
                # runtime "jit_const_*" arrays so kernels are cacheable w.r.t. values.
                if op.name not in param_order:
                    param_order.append(op.name)
            if isinstance(op, LoadVariable):
                for fn in op.field_names or []:
                    if _is_rt_field(str(fn)):
                        if self.on_facet and op.side in ("+", "-"):
                            tag = "pos" if op.side == "+" else "neg"
                            needs_bvec.add(f"bvec_{fn}_{tag}")
                            needs_sign.add(f"sign_{fn}_{tag}")
                            if needs_any_hdiv_div:
                                needs_div.add(f"div_{fn}_{tag}")
                                needs_det.add(f"detJ_{tag}")
                        else:
                            needs_bvec.add(f"bvec_{fn}")
                            needs_sign.add(f"sign_{fn}")
                            if needs_any_hdiv_div:
                                needs_div.add(f"div_{fn}")
                        continue
                    needs_g.add(f"g_{fn}")
                    needs_b.add(f"b_{fn}")
                    if self.on_facet and op.side in ("+", "-"):
                        tag = "pos" if op.side == "+" else "neg"
                        needs_r00.add(f"r00_{fn}_{tag}")
                if op.role == "function":
                    needs_u.add(coeff_array_name(op.name, op.side))
                if sum(op.deriv_order) >= 2:
                    needs_hx = True
                    for fn in op.field_names or []:
                        if _is_rt_field(str(fn)):
                            continue
                        needs_h.update({f"d10_{fn}", f"d01_{fn}", f"d20_{fn}", f"d11_{fn}", f"d02_{fn}"})
                        if self.on_facet:
                            tags: set[str] = set()
                            if op.side in ("+", "-"):
                                tags.add("pos" if op.side == "+" else "neg")
                            if getattr(op, "field_sides", None):
                                tags.update(t for t in op.field_sides if t in ("+", "-"))
                            if not tags:
                                tags.update({"pos", "neg"})
                            for tg in tags:
                                needs_h.update({
                                    f"r10_{fn}_{tg}", f"r01_{fn}_{tg}",
                                    f"r20_{fn}_{tg}", f"r11_{fn}_{tg}", f"r02_{fn}_{tg}",
                                })
            if isinstance(op, (IRHessian, IRLaplacian)):
                needs_hx = True
                for fn in getattr(op, "field_names", []) or []:
                    if _is_rt_field(str(fn)):
                        continue
                    needs_h.update({f"d10_{fn}", f"d01_{fn}", f"d20_{fn}", f"d11_{fn}", f"d02_{fn}"})
                    if self.on_facet:
                        for tg in ("pos", "neg"):
                            needs_h.update({
                                f"r10_{fn}_{tg}", f"r01_{fn}_{tg}",
                                f"r20_{fn}_{tg}", f"r11_{fn}_{tg}", f"r02_{fn}_{tg}",
                            })
            if isinstance(op, LoadElementWiseConstant):
                rank = 1 + len(getattr(op, "tensor_shape", ()) or ())
                ewc_rank[op.name] = rank
                if op.name not in param_order:
                    param_order.append(op.name)
            if isinstance(op, (LoadFacetNormal, LoadFacetNormalComponent)):
                needs_normals = True
            if isinstance(op, CellDiameter):
                needs_cell_diam = True
            if isinstance(op, CheckDomain):
                # Domain restriction flags are provided as uint8 arrays:
                # domain_flag_<bitset_id>[_pos/_neg]
                bitset_id = str(getattr(op, "bitset_id", "") or "")
                if bitset_id:
                    side = str(getattr(op, "side", "") or "")
                    if side == "+":
                        flag = f"domain_flag_{bitset_id}_pos"
                    elif side == "-":
                        flag = f"domain_flag_{bitset_id}_neg"
                    else:
                        flag = f"domain_flag_{bitset_id}"
                    if flag not in param_order:
                        param_order.append(flag)
        for name in sorted(needs_b):
            if name not in param_order:
                param_order.append(name)
        for name in sorted(needs_g):
            if name not in param_order:
                param_order.append(name)
        for name in sorted(needs_bvec):
            if name not in param_order:
                param_order.append(name)
        for name in sorted(needs_div):
            if name not in param_order:
                param_order.append(name)
        for name in sorted(needs_sign):
            if name not in param_order:
                param_order.append(name)
        for name in sorted(needs_det):
            if name not in param_order:
                param_order.append(name)
        for name in sorted(needs_u):
            if name not in param_order:
                param_order.append(name)
        if needs_hx:
            if self.on_facet:
                for base in ("pos_Hxi0", "pos_Hxi1", "neg_Hxi0", "neg_Hxi1"):
                    if base not in param_order:
                        param_order.append(base)
            else:
                for base in ("Hxi0", "Hxi1"):
                    if base not in param_order:
                        param_order.append(base)
        if needs_normals and "normals" not in param_order:
            param_order.append("normals")
        for name in sorted(needs_h):
            if name not in param_order:
                param_order.append(name)
        # If we saw gradient tables but no corresponding basis, add them.
        for name in list(param_order):
            if name.startswith("g_"):
                bname = f"b_{name[2:]}"
                if bname not in param_order:
                    param_order.append(bname)
        if needs_cell_diam:
            if "owner_id" not in param_order:
                param_order.append("owner_id")
            if "h_arr" not in param_order:
                param_order.append("h_arr")
        # Ensure all basis arrays for fields exist (safety net)
        for fn in getattr(self.mixed_element, "field_names", ()):
            if _is_rt_field(str(fn)):
                continue
            bname = f"b_{fn}"
            if bname not in param_order:
                param_order.append(bname)
        for name in sorted(needs_r00):
            if name not in param_order:
                param_order.append(name)
        # Domain flags requested by CheckDomain
        for name in sorted([n for n in required_args if isinstance(n, str) and n.startswith("domain_flag_")]):
            if name not in param_order:
                param_order.append(name)

        module_name = module_name or kernel_name

        # ------------------------------------------------------------------
        # IR-driven C++ emission (subset needed for cavity volume kernels)
        # ------------------------------------------------------------------
        from pycutfem.jit.ir import (
            LoadVariable,
            LoadConstant,
            LoadAnalytic,
            LoadElementWiseConstant,
            Grad,
            Hessian as IRHessian,
            Laplacian as IRLaplacian,
            LoadFacetNormal,
            LoadFacetNormalComponent,
            CellDiameter,
            MeshSize,
            CheckDomain,
            Div,
            BinaryOp,
            Dot,
            Inner,
            Store,
            Trace,
        )

        # local helpers ----------------------------------------------------
        # Field slice policy: mirror the active-field ordering used by the Python
        # codegen (first-seen LoadVariable order, fallback to full element order).
        def _active_fields_from_ir():
            """Gather field ordering as they appear in the IR."""
            seen = set()
            order: list[str] = []
            for op in ir_sequence:
                fns = getattr(op, "field_names", None)
                if fns:
                    for f in fns or []:
                        if f in seen or f not in getattr(self.mixed_element, "field_names", ()):
                            continue
                        seen.add(f)
                        order.append(f)
                elif hasattr(op, "field_name"):
                    f = getattr(op, "field_name", None)
                    if f and f not in seen and f in getattr(self.mixed_element, "field_names", ()):
                        seen.add(f)
                        order.append(f)
            if not order:
                order = list(getattr(self.mixed_element, "field_names", ()))
            return order

        def _active_fields_from_param_order(params: list[str]) -> list[str]:
            """Mirror the active-field order encoded in PARAM_ORDER (b_*, g_*, …)."""
            order: list[str] = []
            for name in params:
                if not isinstance(name, str):
                    continue
                prefix = name.split("_", 1)[0]
                if prefix not in {"b", "g", "d", "r"}:
                    continue
                base = name.split("_", 1)[1] if "_" in name else ""
                # strip sided suffixes (pos/neg) when present
                base = base.split("__", 1)[0]
                if base in getattr(self.mixed_element, "field_names", ()) and base not in order:
                    order.append(base)
            return order

        # Prefer the mirror’s active_fields (Numba) to stay in lockstep with
        # how static args are compressed; fall back to PARAM_ORDER hints, then IR.
        active_fields: list[str] = []
        if self._mirror is not None and getattr(self._mirror, "active_fields", None):
            active_fields = list(self._mirror.active_fields)
        if not active_fields and param_order:
            active_fields = _active_fields_from_param_order(param_order)
        if not active_fields:
            active_fields = _active_fields_from_ir()

        me_fields = list(getattr(self.mixed_element, "field_names", ()))
        if active_fields:
            ordered = [f for f in me_fields if f in active_fields]
            if ordered:
                active_fields = ordered
        else:
            active_fields = me_fields

        # Persist for downstream callers (static arg compression).
        self.active_fields = tuple(active_fields)

        # Default: native component ordering (matches uncompressed gdofs_map)
        field_slices = dict(self.mixed_element.component_dof_slices)

        # When the IR ordering differs from the MixedElement order (subset or
        # permutation), the assembler compresses/reorders static arrays to
        # match the active_fields sequence. Mirror that layout here.
        if tuple(active_fields) != tuple(me_fields):
            field_slices = {}
            offset = 0
            for name in active_fields:
                base_slice = self.mixed_element.component_dof_slices[name]
                width = base_slice.stop - base_slice.start
                field_slices[name] = slice(offset, offset + width)
                offset += width
        functional_dim = 1  # number of output components for functionals

        def new_tmp(prefix="tmp", counter={"i": 0}):
            counter["i"] += 1
            return f"{prefix}_{counter['i']}"

        def combine_side(side_a: str, side_b: str) -> str:
            if side_a and side_b and side_a != side_b:
                return ""
            return side_a or side_b or ""

        def choose_field_sides(fs_a, fs_b):
            return fs_a if fs_a else fs_b

        def resolve_metadata(a: StackItem, b: StackItem, prefer: str | None = None, strict: bool = False):
            """
            Merge (field_names, parent_name, side, field_sides) from two operands.
            Mirrors the helper in pycutfem/jit/codegen.py.
            """
            def default_for(attr: str):
                return [] if attr == "field_names" else ""

            def has_value(item: StackItem, attr: str) -> bool:
                if attr == "field_names":
                    return bool(item.field_names)
                if attr == "parent":
                    return bool(item.parent)
                if attr == "side":
                    return bool(item.side)
                if attr == "field_sides":
                    return any(bool(s) for s in (item.field_sides or []))
                return False

            def pick(attr: str, aval, bval, ahas, bhas):
                if prefer == "a" and ahas: return aval
                if prefer == "b" and bhas: return bval
                if prefer == "basis":
                    a_is_basis = a.role in {"test","trial"}
                    b_is_basis = b.role in {"test","trial"}
                    if a_is_basis and not b_is_basis and ahas: return aval
                    if b_is_basis and not a_is_basis and bhas: return bval
                    if a_is_basis and b_is_basis:
                        if a.role == "test" and ahas: return aval
                        if b.role == "test" and bhas: return bval
                if ahas and not bhas: return aval
                if bhas and not ahas: return bval
                if ahas and bhas:
                    if aval == bval:
                        return aval
                    if strict and attr in ("field_names","parent"):
                        raise ValueError(f"Metadata conflict for '{attr}': {aval} vs {bval}")
                    return default_for(attr)
                return default_for(attr)

            resolved = {}
            resolved["field_names"] = pick("field_names", a.field_names, b.field_names, has_value(a,"field_names"), has_value(b,"field_names"))
            resolved["parent"] = pick("parent", a.parent, b.parent, has_value(a,"parent"), has_value(b,"parent"))
            resolved["side"] = pick("side", a.side, b.side, has_value(a,"side"), has_value(b,"side"))
            fs = pick("field_sides", a.field_sides, b.field_sides, has_value(a,"field_sides"), has_value(b,"field_sides"))
            if not resolved["side"]:
                fs = []
            resolved["field_sides"] = fs
            return resolved["field_names"], resolved["parent"], resolved["side"], resolved["field_sides"]

        def component_side_tag(side: str, field_sides: list, field_name, idx: int) -> str:
            # Match python codegen: explicit side from Jump/Pos/Neg wins.
            if side in ("+", "-"):
                return "pos" if side == "+" else "neg"
            if field_sides and idx < len(field_sides) and field_sides[idx]:
                return field_sides[idx]
            return ""

        def deriv_name(base: str, fn: str, side: str, field_sides: list, idx: int) -> str:
            if not side:
                return f"{base}_{fn}"
            tag = component_side_tag(side, field_sides, fn, idx)
            rbase = "r" + base[1:]  # d10 -> r10 etc.
            return f"{rbase}_{fn}_{tag}"

        class StackItem:
            __slots__ = ("name", "kind", "role", "shape", "field_names", "parent", "side", "field_sides")

            def __init__(self, name, kind, role, shape, field_names=None, parent="", side="", field_sides=None):
                self.name = name
                self.kind = kind  # scalar | mat | grad | vec | const_arr
                self.role = role
                self.shape = shape
                self.field_names = field_names or []
                self.parent = parent  # parent symbol name for functions
                self.side = side or ""
                self.field_sides = field_sides or []

        # Hoist constant-array mapping out of the quadrature loop.
        # NOTE: calling pybind11 APIs (like .request()) inside an OpenMP region
        # is not thread-safe and can segfault. These constants are invariant
        # across elements/quadrature points, so we map them once here.
        const_arr_vars: dict[str, tuple[str, str, tuple[int, ...]]] = {}
        const_arr_setup_lines: list[str] = []
        for op in ir_sequence:
            if not isinstance(op, LoadConstantArray):
                continue
            if op.name in const_arr_vars:
                continue
            if len(op.shape) == 0:
                kind = "scalar"
                shape = ()
            elif len(op.shape) == 1 or getattr(op, "is_vector", False):
                kind = "vec"
                shape = (int(op.shape[0]),)
            else:
                kind = "mat"
                shape = tuple(int(s) for s in op.shape)
            var = f"{op.name}_const"
            buf = f"{op.name}_buf"
            const_arr_vars[op.name] = (var, kind, shape)
            const_arr_setup_lines.append(f"    auto {buf} = {op.name}.request();")
            if kind == "scalar":
                const_arr_setup_lines.append(f"    double {var} = 0.0;")
                const_arr_setup_lines.append(f"    if ({buf}.ndim == 0) {{")
                const_arr_setup_lines.append(f"        {var} = *static_cast<double*>({buf}.ptr);")
                const_arr_setup_lines.append(f"    }} else if ({buf}.ndim == 1 && {buf}.shape[0] >= 1) {{")
                const_arr_setup_lines.append(f"        {var} = static_cast<double*>({buf}.ptr)[0];")
                const_arr_setup_lines.append(f"    }} else if ({buf}.ndim == 2 && {buf}.shape[0] >= 1 && {buf}.shape[1] >= 1) {{")
                const_arr_setup_lines.append(f"        {var} = static_cast<double*>({buf}.ptr)[0];")
                const_arr_setup_lines.append("    } else { throw std::runtime_error(\"Unsupported constant scalar array rank\"); }")
            elif kind == "vec":
                const_arr_setup_lines.append(f"    Eigen::VectorXd {var};")
                const_arr_setup_lines.append(f"    if ({buf}.ndim == 0) {{")
                const_arr_setup_lines.append(f"        double _c = *static_cast<double*>({buf}.ptr);")
                # Prefer the declared shape when available, else fall back to length 1.
                decl_n = int(shape[0]) if shape and int(shape[0]) > 0 else 1
                const_arr_setup_lines.append(f"        {var} = Eigen::VectorXd::Constant({decl_n}, _c);")
                const_arr_setup_lines.append(f"    }} else if ({buf}.ndim == 1) {{")
                const_arr_setup_lines.append(
                    f"        Eigen::Map<const Eigen::VectorXd> _map_{var}(static_cast<double*>({buf}.ptr), {buf}.shape[0]);"
                )
                const_arr_setup_lines.append(f"        {var} = _map_{var};")
                const_arr_setup_lines.append("    } else { throw std::runtime_error(\"Unsupported constant vector array rank\"); }")
            else:
                const_arr_setup_lines.append(f"    Eigen::MatrixXd {var};")
                const_arr_setup_lines.append(f"    if ({buf}.ndim == 0) {{")
                const_arr_setup_lines.append(f"        double _c = *static_cast<double*>({buf}.ptr);")
                const_arr_setup_lines.append(f"        {var} = Eigen::MatrixXd::Constant(1, 1, _c);")
                const_arr_setup_lines.append("    }")
                const_arr_setup_lines.append(f"    else if ({buf}.ndim == 1) {{")
                const_arr_setup_lines.append(
                    f"        Eigen::Map<const Eigen::VectorXd> _map_{var}(static_cast<double*>({buf}.ptr), {buf}.shape[0]);"
                )
                const_arr_setup_lines.append(f"        {var} = _map_{var};")
                const_arr_setup_lines.append("    }")
                const_arr_setup_lines.append(f"    else if ({buf}.ndim == 2) {{")
                const_arr_setup_lines.append(
                    f"        Eigen::Map<const Eigen::Matrix<double, Eigen::Dynamic, Eigen::Dynamic, Eigen::RowMajor>> _map_{var}(static_cast<double*>({buf}.ptr), {buf}.shape[0], {buf}.shape[1]);"
                )
                const_arr_setup_lines.append(f"        {var} = _map_{var};")
                const_arr_setup_lines.append("    }")
                const_arr_setup_lines.append("    else { throw std::runtime_error(\"Unsupported constant matrix array rank\"); }")

        # body generation --------------------------------------------------
        stack: list[StackItem] = []
        body_lines: list[str] = []

        def emit_line(line: str):
            if line.strip():
                body_lines.append("            " + line)

        for idx, op in enumerate(ir_sequence):
            next_op = ir_sequence[idx + 1] if idx + 1 < len(ir_sequence) else None
            if isinstance(op, LoadConstant):
                nm = new_tmp("c")
                emit_line(f"double {nm} = {float(op.value)};")
                stack.append(StackItem(nm, "scalar", "const", ()))
            elif isinstance(op, LoadConstantArray):
                var, kind, shape = const_arr_vars[op.name]
                stack.append(StackItem(var, kind, "const", shape))
            elif isinstance(op, LoadElementWiseConstant):
                required_args.add(op.name)
                required_args.add("owner_id")
                tshape = tuple(op.tensor_shape or ())
                if len(tshape) == 0:
                    nm = new_tmp("ewc")
                    emit_line(f"double {nm} = {op.name}_view(owner_id_view(e));")
                    stack.append(StackItem(nm, "scalar", "value", ()))
                elif len(tshape) == 1:
                    k = int(tshape[0])
                    nm = new_tmp("ewc_vec")
                    emit_line(f"Eigen::VectorXd {nm}({k});")
                    emit_line(f"for (int _i=0; _i<{k}; ++_i) {nm}(_i) = {op.name}_view(owner_id_view(e), _i);")
                    stack.append(StackItem(nm, "vec", "value", (k,)))
                else:
                    raise NotImplementedError("LoadElementWiseConstant only implemented for scalar/vector tensors in C++ backend")
            elif isinstance(op, CellDiameter):
                required_args.add("owner_id")
                required_args.add("h_arr")
                nm = new_tmp("h")
                emit_line(f"double {nm} = h_arr_view(owner_id_view(e));")
                stack.append(StackItem(nm, "scalar", "const", ()))
            elif isinstance(op, MeshSize):
                required_args.add("detJ")
                nm = new_tmp("mesh_h")
                factor = 2.0 if getattr(self.mixed_element.mesh, "element_type", "tri") == "quad" else 1.0
                emit_line(f"double {nm} = {factor} * std::sqrt(std::abs(detJ_view(e,q)));")
                stack.append(StackItem(nm, "scalar", "const", ()))
            elif isinstance(op, LoadFacetNormal):
                nm = new_tmp("nrm")
                emit_line(f"Eigen::Vector2d {nm};")
                emit_line(f"{nm}(0) = normals_view(e,q,0);")
                emit_line(f"{nm}(1) = normals_view(e,q,1);")
                stack.append(StackItem(nm, "vec", "const", (2,)))
            elif isinstance(op, LoadFacetNormalComponent):
                nm = new_tmp("nrm_c")
                emit_line(f"double {nm} = normals_view(e,q,{op.idx});")
                stack.append(StackItem(nm, "scalar", "const", ()))
            elif isinstance(op, CheckDomain):
                a = stack.pop()
                side = op.side or a.side
                if side == "+":
                    flag = f"domain_flag_{op.bitset_id}_pos"
                elif side == "-":
                    flag = f"domain_flag_{op.bitset_id}_neg"
                else:
                    flag = f"domain_flag_{op.bitset_id}"
                required_args.add(flag)
                nm = new_tmp("restricted")
                if a.kind == "scalar" and (a.shape == () or a.shape == tuple()):
                    emit_line(f"double {nm} = {flag}_view(e,q) ? {a.name} : 0.0;")
                elif a.kind in {"vec", "mat"}:
                    emit_line(f"auto {nm} = {a.name};")
                    emit_line(f"if (!{flag}_view(e,q)) {nm}.setZero();")
                elif a.kind in {"grad", "mixed"}:
                    emit_line(f"auto {nm} = {a.name};")
                    emit_line(f"if (!{flag}_view(e,q)) {{")
                    emit_line(f"    for (size_t _i=0; _i<{nm}.size(); ++_i) {nm}[_i].setZero();")
                    emit_line("}")
                elif a.kind == "hess":
                    emit_line(f"auto {nm} = {a.name};")
                    if a.role in {"test", "trial"}:
                        emit_line(f"if (!{flag}_view(e,q)) {{")
                        emit_line(f"    for (size_t _i=0; _i<{nm}.size(); ++_i) {nm}[_i].setZero();")
                        emit_line("}")
                    else:
                        emit_line(f"if (!{flag}_view(e,q)) {nm}.setZero();")
                else:
                    emit_line(f"auto {nm} = {a.name};")
                    emit_line(f"if (!{flag}_view(e,q)) {nm}.setZero();")

                # Apply per-field restriction masks on facet integrals (ghost/interface)
                if (
                    self.on_facet
                    and side in ("+", "-")
                    and a.field_names
                    and any(dim == -1 for dim in a.shape)
                ):
                    # Some vector-valued fields (e.g. H(div) RT) use a single
                    # field name for multiple value components. Apply the same
                    # restriction mask to every component row.
                    try:
                        n_comp = int(a.shape[0]) if (len(a.shape) >= 1 and int(a.shape[0]) > 0) else int(len(a.field_names))
                    except Exception:
                        n_comp = int(len(a.field_names))

                    for idx in range(n_comp):
                        fld = a.field_names[idx] if idx < len(a.field_names) else a.field_names[0]
                        side_tag = component_side_tag(side, a.field_sides or [], fld, idx)
                        if side_tag not in ("pos", "neg"):
                            side_tag = "pos" if side == "+" else "neg"
                        mask = f"restrict_mask_{fld}_{side_tag}"
                        required_args.add(mask)
                        if a.kind == "mat":
                            emit_line(f"for (ssize_t j=0; j<n_union; ++j) {nm}({idx}, j) *= {mask}_view(e, j);")
                        elif a.kind in {"grad", "mixed", "hess"}:
                            emit_line(f"for (ssize_t j=0; j<n_union; ++j) {{")
                            emit_line(f"    double w = {mask}_view(e, j);")
                            emit_line(f"    for (ssize_t d=0; d<{nm}[{idx}].cols(); ++d) {nm}[{idx}](j, d) *= w;")
                            emit_line("}")
                stack.append(StackItem(nm, a.kind, a.role, a.shape, a.field_names, a.parent, a.side, a.field_sides))
            elif isinstance(op, LoadAnalytic):
                name = f"ana_{op.func_id}"
                required_args.add(name)
                view_name = f"{name}_view"
                # Avoid collisions with parameter names like "ana_0"/"ana_1".
                nm = new_tmp("ana_val")
                if len(op.tensor_shape or ()) == 0:
                    emit_line(f"double {nm} = {view_name}(e,q);")
                    stack.append(StackItem(nm, "scalar", "const", ()))
                elif len(op.tensor_shape) == 1:
                    k = int(op.tensor_shape[0])
                    emit_line(f"Eigen::VectorXd {nm}({k});")
                    for ii in range(k):
                        emit_line(f"{nm}({ii}) = {view_name}(e,q,{ii});")
                    stack.append(StackItem(nm, "vec", "const", (k,)))
                elif len(op.tensor_shape) == 2:
                    r = int(op.tensor_shape[0])
                    c = int(op.tensor_shape[1])
                    emit_line(f"Eigen::Matrix<double,{r},{c}> {nm};")
                    for ii in range(r):
                        for jj in range(c):
                            emit_line(f"{nm}({ii},{jj}) = {view_name}(e,q,{ii},{jj});")
                    stack.append(StackItem(nm, "mat", "const", (r, c)))
                else:
                    raise NotImplementedError(
                        "LoadAnalytic only implemented for scalar/vector/matrix tensors in C++ backend"
                    )
            elif isinstance(op, LoadVariable):
                followed_by_diff = isinstance(next_op, (Grad,))
                # ------------------------------------------------------------------
                # Functions / coefficients: treat like "value" (gather with basis)
                # ------------------------------------------------------------------
                if op.role == "function" and op.deriv_order == (0, 0):
                    coeff_name = coeff_array_name(op.name, op.side)
                    required_args.add(coeff_name)
                    apply_restrict_mask = isinstance(next_op, CheckDomain)
                    hdiv_field = None
                    if op.field_names and len(op.field_names) == 1:
                        cand = str(op.field_names[0])
                        if getattr(self.mixed_element, "_field_families", {}).get(cand, None) == "RT":
                            hdiv_field = cand
                    if hdiv_field is not None:
                        if op.side == "+":
                            required_args.add("J_inv_pos")
                        elif op.side == "-":
                            required_args.add("J_inv_neg")
                        else:
                            required_args.add("J_inv")
                        side_tag = "pos" if op.side == "+" else "neg" if op.side == "-" else ""
                        bvec_name = f"bvec_{hdiv_field}_{side_tag}" if side_tag else f"bvec_{hdiv_field}"
                        sign_name = f"sign_{hdiv_field}_{side_tag}" if side_tag else f"sign_{hdiv_field}"
                        required_args.update({bvec_name, sign_name})
                        nm = new_tmp("hdiv_val")
                        emit_line(f"Eigen::VectorXd {nm}(2);")
                        emit_line(f"{nm}.setZero();")
                        adj = new_tmp("adj")
                        emit_line(f"Eigen::Matrix<double,2,2> {adj};")
                        jloc_var = "Jloc_pos" if op.side == "+" else "Jloc_neg" if op.side == "-" else "Jloc"
                        emit_line(f"{adj}(0,0)={jloc_var}(1,1); {adj}(0,1)=-{jloc_var}(0,1);")
                        emit_line(f"{adj}(1,0)=-{jloc_var}(1,0); {adj}(1,1)={jloc_var}(0,0);")
                        emit_line("for (ssize_t j=0; j<n_union; ++j) {")
                        emit_line(f"    double s = {sign_name}_view(e, j);")
                        emit_line(f"    double bh0 = {bvec_name}_view(e, q, 0, j);")
                        emit_line(f"    double bh1 = {bvec_name}_view(e, q, 1, j);")
                        emit_line(f"    double b0 = {adj}(0,0)*bh0 + {adj}(0,1)*bh1;")
                        emit_line(f"    double b1 = {adj}(1,0)*bh0 + {adj}(1,1)*bh1;")
                        emit_line(f"    double coeff = {coeff_name}_view(e, j);")
                        emit_line(f"    {nm}(0) += coeff * (b0 * s);")
                        emit_line(f"    {nm}(1) += coeff * (b1 * s);")
                        emit_line("}")
                        stack.append(StackItem(nm, "vec", "value", (2,), op.field_names, op.name, op.side, op.field_sides or []))
                        continue
                    k = len(op.field_names)
                    nm = new_tmp("val")
                    emit_line(f"Eigen::VectorXd {nm}({k});")
                    emit_line(f"{nm}.setZero();")
                    for idx, fn in enumerate(op.field_names):
                        s0 = field_slices.get(fn, slice(0, 0)).start
                        s1 = field_slices.get(fn, slice(0, 0)).stop
                        side_tag = component_side_tag(op.side, getattr(op, "field_sides", None), fn, idx)
                        if self.on_facet and side_tag in ("pos", "neg"):
                            basis_name = f"r00_{fn}_{side_tag}"
                            map_name   = f"{side_tag}_map_{fn}"
                            required_args.update({basis_name, map_name})
                            mask_name = None
                            if apply_restrict_mask:
                                mask_name = f"restrict_mask_{fn}_{side_tag}"
                                required_args.add(mask_name)
                            emit_line(f"{nm}({idx}) = 0.0;")
                            map_len = new_tmp("map_len"); basis_len = new_tmp("basis_len")
                            emit_line(f"ssize_t {map_len} = {map_name}_view.shape(1);")
                            emit_line(f"ssize_t {basis_len} = {basis_name}_view.shape(2);")
                            emit_line(f"for (ssize_t j=0; j<{map_len}; ++j) {{")
                            emit_line(f"    int col = {map_name}_view(e, j);")
                            emit_line(f"    if (col < 0 || col >= n_union) continue;")
                            emit_line(
                                f"    ssize_t src = ({basis_len} == {map_len}) ? j : (({basis_len} == n_union) ? col : {s0} + j);"
                            )
                            emit_line(f"    if (src >= {basis_len}) continue;")
                            emit_line(f"    double coeff = {coeff_name}_view(e, col);")
                            if mask_name is not None:
                                emit_line(f"    coeff *= {mask_name}_view(e, col);")
                            emit_line(f"    {nm}({idx}) += coeff * {basis_name}_view(e, q, src);")
                            emit_line("}")
                        else:
                            s0_adj = new_tmp("s0"); s1_adj = new_tmp("s1")
                            emit_line(f"ssize_t {s0_adj} = ({s0} < 0 || {s0} >= n_union) ? 0 : {s0};")
                            emit_line(f"ssize_t {s1_adj} = ({s1} < 0 || {s1} > n_union) ? n_union : {s1};")
                            emit_line(f"if ({s1_adj} < {s0_adj}) {s1_adj} = {s0_adj};")
                            emit_line(
                                f"{nm}({idx}) = load_variable_component({coeff_name}_view, b_{fn}_view, e, q, {s0_adj}, {s1_adj});"
                            )
                    if k == 1:
                        emit_line(f"double {nm}_scalar = {nm}(0);")
                        stack.append(StackItem(f"{nm}_scalar", "scalar", "value", (), op.field_names, op.name, op.side, op.field_sides or []))
                    else:
                        stack.append(StackItem(nm, "vec", "value", (k,), op.field_names, op.name, op.side, op.field_sides or []))
                elif op.role in {"test", "trial"} and op.deriv_order == (0, 0):
                    if followed_by_diff:
                        stack.append(
                            StackItem("__basis__", "basis_ref", op.role, (len(op.field_names), -1), op.field_names, op.name, op.side, op.field_sides)
                        )
                        continue
                    hdiv_field = None
                    if op.field_names and len(op.field_names) == 1:
                        cand = str(op.field_names[0])
                        if getattr(self.mixed_element, "_field_families", {}).get(cand, None) == "RT":
                            hdiv_field = cand
                    if hdiv_field is not None:
                        side_tag = "pos" if op.side == "+" else "neg" if op.side == "-" else ""
                        bvec_name = f"bvec_{hdiv_field}_{side_tag}" if side_tag else f"bvec_{hdiv_field}"
                        sign_name = f"sign_{hdiv_field}_{side_tag}" if side_tag else f"sign_{hdiv_field}"
                        required_args.update({bvec_name, sign_name})
                        nm = new_tmp("hdiv_basis")
                        emit_line(f"Eigen::MatrixXd {nm}(2, n_union);")
                        emit_line(f"{nm}.setZero();")
                        adj = new_tmp("adj")
                        emit_line(f"Eigen::Matrix<double,2,2> {adj};")
                        jloc_var = "Jloc_pos" if op.side == "+" else "Jloc_neg" if op.side == "-" else "Jloc"
                        emit_line(f"{adj}(0,0)={jloc_var}(1,1); {adj}(0,1)=-{jloc_var}(0,1);")
                        emit_line(f"{adj}(1,0)=-{jloc_var}(1,0); {adj}(1,1)={jloc_var}(0,0);")
                        emit_line("for (ssize_t j=0; j<n_union; ++j) {")
                        emit_line(f"    double s = {sign_name}_view(e, j);")
                        emit_line(f"    double bh0 = {bvec_name}_view(e, q, 0, j);")
                        emit_line(f"    double bh1 = {bvec_name}_view(e, q, 1, j);")
                        emit_line(f"    double b0 = {adj}(0,0)*bh0 + {adj}(0,1)*bh1;")
                        emit_line(f"    double b1 = {adj}(1,0)*bh0 + {adj}(1,1)*bh1;")
                        emit_line(f"    {nm}(0, j) = b0 * s;")
                        emit_line(f"    {nm}(1, j) = b1 * s;")
                        emit_line("}")
                        stack.append(StackItem(nm, "mat", op.role, (2, -1), op.field_names, op.name, op.side, op.field_sides))
                        continue
                    k = len(op.field_names)
                    nm = new_tmp("basis")
                    emit_line(f"Eigen::MatrixXd {nm}({k}, n_union);")
                    emit_line(f"{nm}.setZero();")
                    for idx2, fn in enumerate(op.field_names):
                        side_tag = component_side_tag(op.side, getattr(op, "field_sides", None), fn, idx2)
                        use_side = (self.on_facet and side_tag in ("pos", "neg"))
                        basis_name = f"r00_{fn}_{side_tag}" if use_side else f"b_{fn}"
                        map_name   = f"{side_tag}_map_{fn}" if use_side else None
                        s0 = field_slices.get(fn, slice(0, 0)).start
                        if use_side:
                            required_args.update({basis_name, map_name})
                            map_len = new_tmp("map_len")
                            basis_len = new_tmp("basis_len")
                            emit_line(f"ssize_t {map_len} = {map_name}_view.shape(1);")
                            emit_line(f"ssize_t {basis_len} = {basis_name}_view.shape(2);")
                            emit_line(f"for (ssize_t j=0; j<{map_len}; ++j) {{")
                            emit_line(f"    int col = {map_name}_view(e, j);")
                            emit_line(f"    if (col < 0 || col >= n_union) continue;")
                            emit_line(
                                f"    ssize_t src = ({basis_len} == {map_len}) ? j : (({basis_len} == n_union) ? col : {s0} + j);"
                            )
                            emit_line(f"    if (src >= {basis_len}) continue;")
                            emit_line(f"    {nm}({idx2}, col) = {basis_name}_view(e, q, src);")
                            emit_line("}")
                        else:
                            emit_line(
                                f"for (ssize_t j=0; j<n_union; ++j) {nm}({idx2}, j) = {basis_name}_view(e, q, j);"
                            )
                    stack.append(StackItem(nm, "mat", op.role, (k, -1), op.field_names, op.name, op.side, op.field_sides))
                elif op.role in {"test", "trial"} and op.deriv_order in {(1, 0), (0, 1)}:
                    # First derivative requested directly (no Grad op).
                    # Build a (k x n_union) matrix with the selected physical component.
                    if op.side == "+":
                        required_args.add("J_inv_pos")
                    elif op.side == "-":
                        required_args.add("J_inv_neg")
                    else:
                        required_args.add("J_inv")
                    k = len(op.field_names)
                    nm = new_tmp("d1")
                    emit_line(f"Eigen::MatrixXd {nm}({k}, n_union);")
                    emit_line(f"{nm}.setZero();")
                    comp = 0 if op.deriv_order == (1, 0) else 1
                    jloc_var = "Jloc_pos" if op.side == "+" else "Jloc_neg" if op.side == "-" else "Jloc"
                    for idx2, fn in enumerate(op.field_names):
                        s0 = field_slices.get(fn, slice(0, 0)).start
                        side_tag = component_side_tag(op.side, getattr(op, "field_sides", None), fn, idx2)
                        bool_sided = self.on_facet and side_tag in ("pos", "neg")
                        if bool_sided:
                            d10 = f"r10_{fn}_{side_tag}"
                            d01 = f"r01_{fn}_{side_tag}"
                            map_name = f"{side_tag}_map_{fn}"
                            required_args.update({d10, d01, map_name})
                            map_len = new_tmp("map_len")
                            basis_len = new_tmp("basis_len")
                            emit_line(f"ssize_t {map_len} = {map_name}_view.shape(1);")
                            emit_line(f"ssize_t {basis_len} = {d10}_view.shape(2);")
                            emit_line(f"for (ssize_t j=0; j<{map_len}; ++j) {{")
                            emit_line(f"    int col = {map_name}_view(e, j);")
                            emit_line(f"    if (col < 0 || col >= n_union) continue;")
                            emit_line(
                                f"    ssize_t src = ({basis_len} == {map_len}) ? j : (({basis_len} == n_union) ? col : {s0} + j);"
                            )
                            emit_line(f"    if (src >= {basis_len}) continue;")
                            emit_line(f"    double gr0 = {d10}_view(e,q,src); double gr1 = {d01}_view(e,q,src);")
                            emit_line(f"    double px = gr0*{jloc_var}(0,0) + gr1*{jloc_var}(1,0);")
                            emit_line(f"    double py = gr0*{jloc_var}(0,1) + gr1*{jloc_var}(1,1);")
                            emit_line(f"    {nm}({idx2}, col) = ({comp}==0 ? px : py);")
                            emit_line("}")
                        else:
                            gname = f"g_{fn}_view"
                            required_args.add(f"g_{fn}")
                            emit_line(f"for (ssize_t j=0; j<n_union; ++j) {{")
                            emit_line(f"    double gx = {gname}(e,q,j,0); double gy = {gname}(e,q,j,1);")
                            emit_line(f"    double px = gx*{jloc_var}(0,0) + gy*{jloc_var}(1,0);")
                            emit_line(f"    double py = gx*{jloc_var}(0,1) + gy*{jloc_var}(1,1);")
                            emit_line(f"    {nm}({idx2}, j) = ({comp}==0 ? px : py);")
                            emit_line("}")
                    stack.append(StackItem(nm, "mat", op.role, (k, -1), op.field_names, op.name, op.side, op.field_sides))
                elif op.role in {"test", "trial"} and op.deriv_order in {(2, 0), (1, 1), (0, 2)}:
                    k = len(op.field_names)
                    nm = new_tmp("d2")
                    emit_line(f"Eigen::MatrixXd {nm}({k}, n_union);")
                    emit_line(f"{nm}.setZero();")
                    for idx2, fn in enumerate(op.field_names):
                        side_tag = component_side_tag(op.side, getattr(op, "field_sides", None), fn, idx2)
                        use_side = (self.on_facet and side_tag in ("pos", "neg"))
                        base = "r" if use_side else "d"
                        deriv_key = "20" if op.deriv_order == (2, 0) else "11" if op.deriv_order == (1, 1) else "02"
                        table_name = f"{base}{deriv_key}_{fn}" + (f"_{side_tag}" if use_side else "")
                        map_name = f"{side_tag}_map_{fn}" if use_side else None
                        s0 = field_slices.get(fn, slice(0, 0)).start
                        if use_side:
                            map_len = new_tmp("map_len")
                            basis_len = new_tmp("basis_len")
                            emit_line(f"ssize_t {map_len} = {map_name}_view.shape(1);")
                            emit_line(f"ssize_t {basis_len} = {table_name}_view.shape(2);")
                            emit_line(f"for (ssize_t j=0; j<{map_len}; ++j) {{")
                            emit_line(f"    int col = {map_name}_view(e, j);")
                            emit_line(f"    if (col < 0 || col >= n_union) continue;")
                            emit_line(
                                f"    ssize_t src = ({basis_len} == {map_len}) ? j : (({basis_len} == n_union) ? col : {s0} + j);"
                            )
                            emit_line(f"    if (src >= {basis_len}) continue;")
                            emit_line(f"    {nm}({idx2}, col) = {table_name}_view(e, q, src);")
                            emit_line("}")
                    else:
                        emit_line(f"for (ssize_t j=0; j<n_union; ++j) {nm}({idx2}, j) = {table_name}_view(e, q, j);")
                    stack.append(StackItem(nm, "mat", op.role, (k, -1), op.field_names, op.name, op.side, op.field_sides))
                elif op.role == "function":
                    # Coefficient derivatives (from UFL Derivative nodes) arrive here as
                    # LoadVariable(role="function", deriv_order != (0,0)).
                    #
                    # IMPORTANT: Do not treat these as value lookups. We must evaluate
                    # the requested physical derivative at the quadrature point using
                    # the reference gradient tables and J^{-1}. Otherwise H1-seminorm
                    # error norms become mesh-independent ("flat" convergence).
                    coeff_name = coeff_array_name(op.name, op.side)
