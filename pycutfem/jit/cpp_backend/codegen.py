                            emit_line(f"ssize_t {basis_len} = {d10}_view.shape(2);")
                            emit_line(f"for (ssize_t j=0; j<{map_len}; ++j) {{")
                            emit_line(f"    int col = {map_name}_view(e, j);")
                            emit_line(f"    if (col < 0 || col >= n_union) continue;")
