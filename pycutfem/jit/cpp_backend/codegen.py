                    if op.side == "+":
                        required_args.add("J_inv_pos")
                    elif op.side == "-":
                        required_args.add("J_inv_neg")
                    else:
                        required_args.add("J_inv")
                    jloc_var = "Jloc_pos" if op.side == "+" else "Jloc_neg" if op.side == "-" else "Jloc"

